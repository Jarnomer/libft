#include <libft.h>

void	ft_putnbr_fd(int n, int fd)
{
	if (n == INT_MIN)
		return (void)(ft_putstr_fd("-2147483648", fd));
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}
